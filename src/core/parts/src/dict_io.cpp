// ---- Augment-set membership predicate ----------------------------

const char* const kConnectors[] = {"-", "'", "&", ",", ".", "$"};

bool is_connector(std::string_view v) {
  if (v.size() != 1) return false;
  for (const char* c : kConnectors) {
    if (v[0] == c[0]) return true;
  }
  return false;
}

// True if the part would be regenerated by augment_with_atoms().
// Save filters these out; load skips writing them (none in file).
bool is_augmented(const PartKey& k) {
  if (k.kind == Kind::Letter) return true;
  if (k.kind == Kind::Whole && k.value.size() == 1) {
    const char c = k.value[0];
    if (c >= 'a' && c <= 'z') return true;
    if (c >= '0' && c <= '9') return true;
    if (is_connector(k.value)) return true;
  }
  if (k.kind == Kind::Delimiter && is_connector(k.value)) {
    return true;
  }
  return false;
}

// ---- Text escape / unescape (for delimiters) ---------------------

std::string escape_value(std::string_view s) {
  std::string out;
  out.reserve(s.size());
  for (unsigned char c : s) {
    switch (c) {
      case '\n': out += "\\n";  break;
      case '\t': out += "\\t";  break;
      case '\r': out += "\\r";  break;
      case '\\': out += "\\\\"; break;
      default:
        if (c < 0x20 || c == 0x7f) {
          char buf[5];
          std::snprintf(buf, sizeof(buf), "\\x%02X", c);
          out += buf;
        } else {
          out += static_cast<char>(c);
        }
    }
  }
  return out;
}

int hex_digit(char c) {
  if (c >= '0' && c <= '9') return c - '0';
  if (c >= 'a' && c <= 'f') return c - 'a' + 10;
  if (c >= 'A' && c <= 'F') return c - 'A' + 10;
  return -1;
}

std::string unescape_value(std::string_view s) {
  std::string out;
  out.reserve(s.size());
  for (std::size_t i = 0; i < s.size(); ) {
    if (s[i] != '\\') {
      out += s[i++];
      continue;
    }
    if (i + 1 >= s.size()) {
      out += '\\';
      ++i;
      continue;
    }
    const char esc = s[i + 1];
    switch (esc) {
      case 'n':  out += '\n'; i += 2; break;
      case 't':  out += '\t'; i += 2; break;
      case 'r':  out += '\r'; i += 2; break;
      case '\\': out += '\\'; i += 2; break;
      case 'x': {
        if (i + 3 < s.size()) {
          const int h = hex_digit(s[i + 2]);
          const int l = hex_digit(s[i + 3]);
          if (h >= 0 && l >= 0) {
            out += static_cast<char>((h << 4) | l);
            i += 4;
            break;
          }
        }
        out += '\\';
        ++i;
        break;
      }
      default:
        out += '\\';
        ++i;
        break;
    }
  }
  return out;
}

// ---- ## convention helpers ---------------------------------------

bool starts_with_hashhash(std::string_view s) {
  return s.size() >= 2 && s[0] == '#' && s[1] == '#';
}
bool ends_with_hashhash(std::string_view s) {
  const std::size_t n = s.size();
  return n >= 2 && s[n - 2] == '#' && s[n - 1] == '#';
}

