namespace {

constexpr std::array<char, 4> kMagicBDI2 = {'B', 'D', 'I', '2'};
constexpr std::uint8_t kBinaryVersion = 1;

void write_u8 (std::ostream& out, std::uint8_t  v) {
  out.put(static_cast<char>(v));
}
void write_u16(std::ostream& out, std::uint16_t v) {
  const char b[2] = {
    static_cast<char>(v & 0xff),
    static_cast<char>((v >> 8) & 0xff),
  };
  out.write(b, 2);
}
void write_bytes(std::ostream& out, const std::string& s) {
  if (!s.empty()) out.write(s.data(), static_cast<std::streamsize>(s.size()));
}

bool read_u8(std::istream& in, std::uint8_t& v) {
  char c;
  if (!in.get(c)) return false;
  v = static_cast<std::uint8_t>(static_cast<unsigned char>(c));
  return true;
}
bool read_u16(std::istream& in, std::uint16_t& v) {
  unsigned char b[2];
  in.read(reinterpret_cast<char*>(b), 2);
  if (in.gcount() != 2) return false;
  v = static_cast<std::uint16_t>(b[0]) |
      (static_cast<std::uint16_t>(b[1]) << 8);
  return true;
}

std::uint8_t kind_to_byte(Kind k) {
  switch (k) {
    case Kind::Start:     return 0;
    case Kind::End:       return 1;
    case Kind::Mid:       return 2;
    case Kind::Letter:    return 3;
    case Kind::Whole:     return 4;
    case Kind::Delimiter: return 5;
  }
  return 255;
}

bool byte_to_kind(std::uint8_t b, Kind& out) {
  switch (b) {
    case 0: out = Kind::Start;     return true;
    case 1: out = Kind::End;       return true;
    case 2: out = Kind::Mid;       return true;
    case 3: out = Kind::Letter;    return true;
    case 4: out = Kind::Whole;     return true;
    case 5: out = Kind::Delimiter; return true;
    default: return false;
  }
}

}  // namespace

void save_dict_binary(const PartDictionary& dict, std::ostream& out) {
  // Header.
  out.write(kMagicBDI2.data(), 4);
  write_u8(out, kBinaryVersion);
  write_u8(out, 0);            // reserved
  write_u16(out, 0);           // reserved

  // Records.
  for (const PartKey& k : dict.all_parts()) {
    if (is_augmented(k)) continue;
    if (k.value.size() > 0xffff) {
      throw std::runtime_error("part value exceeds uint16 length");
    }
    write_u8(out, kind_to_byte(k.kind));
    write_u16(out, static_cast<std::uint16_t>(k.value.size()));
    write_bytes(out, k.value);
  }
}

PartDictionary load_dict_binary(std::istream& in) {
  // Header.
  char magic[4];
  in.read(magic, 4);
  if (in.gcount() != 4 ||
      std::memcmp(magic, kMagicBDI2.data(), 4) != 0) {
    throw std::runtime_error("load_dict_binary: bad magic (expected BDI2)");
  }
  std::uint8_t version = 0, reserved8 = 0;
  std::uint16_t reserved16 = 0;
  if (!read_u8(in, version) ||
      !read_u8(in, reserved8) ||
      !read_u16(in, reserved16)) {
    throw std::runtime_error("load_dict_binary: truncated header");
  }
  if (version != kBinaryVersion) {
    std::ostringstream msg;
    msg << "load_dict_binary: unsupported version " << static_cast<int>(version);
    throw std::runtime_error(msg.str());
  }

  PartDictionary dict;

  while (true) {
    std::uint8_t kind_byte = 0;
    if (!read_u8(in, kind_byte)) break;  // clean EOF
    Kind kind;
    if (!byte_to_kind(kind_byte, kind)) {
      throw std::runtime_error("load_dict_binary: bad kind byte");
    }
    std::uint16_t len = 0;
    if (!read_u16(in, len)) {
      throw std::runtime_error("load_dict_binary: truncated length");
    }
    std::string value(len, '\0');
    if (len > 0) {
      in.read(value.data(), len);
      if (in.gcount() != static_cast<std::streamsize>(len)) {
        throw std::runtime_error("load_dict_binary: truncated value");
      }
    }
    dict.add(kind, std::move(value));
  }

  augment_with_atoms(dict);
  return dict;
}

