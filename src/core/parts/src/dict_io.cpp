// TEXT
// =============================================================================

void save_dict_text(const PartDictionary& dict, std::ostream& out) {
  // Pass 1: Whole / Start / Mid / End in dict ID order (so reload
  // reproduces the same insertion order).
  for (const PartKey& k : dict.all_parts()) {
    if (is_augmented(k)) continue;
    switch (k.kind) {
      case Kind::Whole:
        out << k.value << '\n';
        break;
      case Kind::Start:
        out << k.value << "##\n";
        break;
      case Kind::Mid:
        out << "##" << k.value << "##\n";
        break;
      case Kind::End:
        out << "##" << k.value << '\n';
        break;
      case Kind::Letter:
      case Kind::Delimiter:
        // handled below or filtered above
        break;
    }
  }

  // Pass 2: Delimiter section.
  out << "[delim]\n";
  for (const PartKey& k : dict.all_parts()) {
    if (k.kind != Kind::Delimiter) continue;
    if (is_augmented(k)) continue;
    out << escape_value(k.value) << '\n';
  }
}

PartDictionary load_dict_text(std::istream& in) {
  PartDictionary dict;
  std::string line;
  bool in_delim = false;

  while (std::getline(in, line)) {
    // Strip trailing CR (Windows line endings).
    while (!line.empty() && (line.back() == '\r')) line.pop_back();
    if (line.empty()) continue;

    if (line == "[delim]") {
      in_delim = true;
      continue;
    }

    if (in_delim) {
      dict.add(Kind::Delimiter, unescape_value(line));
      continue;
    }

    const bool s = starts_with_hashhash(line);
    const bool e = ends_with_hashhash(line);
    if (s && e) {
      if (line.size() >= 4) {
        dict.add(Kind::Mid, line.substr(2, line.size() - 4));
      }
    } else if (e) {
      dict.add(Kind::Start, line.substr(0, line.size() - 2));
    } else if (s) {
      dict.add(Kind::End, line.substr(2));
    } else {
      dict.add(Kind::Whole, line);
    }
  }

  augment_with_atoms(dict);
  return dict;
}

